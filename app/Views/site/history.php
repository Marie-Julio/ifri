							L’Institut de Formation et de Recherche en Informatique est né des cendres du Centre de Formation et de Recherche en Information (CEFRI) créé en 2008 par arrêté rectoral dans le cadre de la coopération entre l’Université d’Abomey-Calavi et l’Académie de Recherche et d’Enseignement Supérieur (ARES) de la Belgique. Ce centre a été dirigé par le Professeur Norbert M. Hounkonnou, Président de la Chaire Internationale de Physique Mathématique et Applications (CIPMA). Les formations offertes étaient la licence professionnelle en Informatique Générale, le master de recherche en Informatique Générale. Les formations étaient animées par des enseignants étrangers venant principalement des universités belges sur financement de l’ARES et par certains enseignants locaux. Cette expérience a duré jusqu’en avril 2013 pour succéder à l’Institut de Formation et de Recherche en Informatique. <br>

